                <p><span>
                Unter dem Titel SENSEVEN eröffnet der siebte Jahrgang des Studiengangs Kunst und Multimedia am 14. Juli um 19 Uhr 
                seine Abschlussausstellung im Studio 2 des Muffatwerks in München. Die Ausstellung findet in Kooperation mit dem Gärtnerplatz OpenAir statt.
                </span></p>

                    <p>
                        Bis zum 17. Juli zeigen 17 Studenten ihre  – aus einem Jahr intensiver Arbeit entstandenen – 
                        künstlerischen Projekte in einer multimedialen Ausstellung. Das Spektrum reicht von interaktiven E-Books, 
                        Websites, Apps, Augmented Reality, Games und Filmen bis hin zu Mischformen aus allen Kategorien, die sich zu einem vielseitigen Gesamtbild zusammenfügen. 
                    </p>
                    <p>
                        <strong>Sehen, Fühlen, Hören, Schmecken, Riechen, intuitives Wahrnehmen</strong> - 
                        SENSEVEN steht für die Vereinigung aller Sinne in einem, dem 7. Sinn. 
                        Die interdisziplinären Arbeiten schaffen eine Brücke zwischen moderner Technik und 
                        künstlerischer Gestaltung und führen den Betrachter in eine Welt, die er mit allen Sinnen 
                        erfahren kann. <br/><br/>
                    </p>
